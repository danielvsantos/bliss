/**
 * L2 — ANNUAL tier addendum.
 */

function buildTierAddendum() {
  return `TIER: Year-in-review for a closed calendar year.
- This is the user's most-read insight of the year. Make it count.
- Compare to prior year. Highlight year-over-year shifts.
- Each body: 4-5 sentences with 4-6 numbers.
- Lead with the single most defining fact of the year for that lens, then context, then trajectory into the new year.
- Celebrate milestones plainly when they occurred (net worth crossing thresholds, debt principal substantially down, savings rate sustained at a new level).
- Title: ≤12 words.`;
}

module.exports = { buildTierAddendum };
